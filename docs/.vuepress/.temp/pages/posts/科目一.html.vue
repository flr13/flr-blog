<h3 id="二、交通标志与标线" tabindex="-1"><a class="header-anchor" href="#二、交通标志与标线" aria-hidden="true">#</a> <strong>二、交通标志与标线</strong></h3>
<h4 id="_1-标志分类与示例" tabindex="-1"><a class="header-anchor" href="#_1-标志分类与示例" aria-hidden="true">#</a> <strong>1. 标志分类与示例</strong></h4>
<ul>
<li><strong>警告标志</strong>（黄底黑边，三角形）：
<ul>
<li>注意行人、交叉路口、急弯路、连续弯路、陡坡、学校区域。</li>
</ul>
</li>
<li><strong>禁令标志</strong>（红圈白底，圆形/八角形）：
<ul>
<li>禁止通行、禁止停车、限速、禁止鸣笛、停车让行（八角形）。</li>
</ul>
</li>
<li><strong>指示标志</strong>（蓝底白图案，圆形/矩形）：
<ul>
<li>直行、左转、机动车道、非机动车道、人行横道。</li>
</ul>
</li>
<li><strong>指路标志</strong>（蓝/绿底白字）：
<ul>
<li>地点方向、高速公路入口、服务区预告、收费站预告。</li>
</ul>
</li>
</ul>
<h4 id="_2-标线详解" tabindex="-1"><a class="header-anchor" href="#_2-标线详解" aria-hidden="true">#</a> <strong>2. 标线详解</strong></h4>
<ul>
<li><strong>纵向标线</strong>：
<ul>
<li><strong>黄色实线</strong>：禁止跨越（分隔对向车道或路缘禁停线）。</li>
<li><strong>白色虚线</strong>：允许变道，但需确保安全。</li>
</ul>
</li>
<li><strong>横向标线</strong>：
<ul>
<li><strong>停止线</strong>：白色实线，红灯时需停车等候。</li>
<li><strong>人行横道线</strong>：白色平行粗实线（斑马线）。</li>
</ul>
</li>
<li><strong>特殊标线</strong>：
<ul>
<li><strong>导流线</strong>（白色V形/斜纹）：禁止压线或停车。</li>
<li><strong>减速标线</strong>（白色菱形块）：提示减速。</li>
</ul>
</li>
</ul>
<h4 id="_3-交通信号灯" tabindex="-1"><a class="header-anchor" href="#_3-交通信号灯" aria-hidden="true">#</a> <strong>3. 交通信号灯</strong></h4>
<ul>
<li><strong>红灯</strong>：禁止通行（右转车辆在无单独箭头灯时可通行，但需礼让行人）。</li>
<li><strong>黄灯</strong>：已过停止线的车辆可继续通过，未过的需停车。</li>
<li><strong>绿灯</strong>：允许通行，但需观察路口情况。</li>
</ul>
