<h3 id="三、安全驾驶与应急处理" tabindex="-1"><a class="header-anchor" href="#三、安全驾驶与应急处理" aria-hidden="true">#</a> <strong>三、安全驾驶与应急处理</strong></h3>
<h4 id="_1-不同场景驾驶规则" tabindex="-1"><a class="header-anchor" href="#_1-不同场景驾驶规则" aria-hidden="true">#</a> <strong>1. 不同场景驾驶规则</strong></h4>
<ul>
<li><strong>夜间行车</strong>：
<ul>
<li>近光灯使用（150米内），会车时切换近光，无路灯时开远光但需会车切换。</li>
<li>车速限制：无路灯道路≤30km/h。</li>
</ul>
</li>
<li><strong>雨天/雾天</strong>：
<ul>
<li>开启雾灯+近光灯+危险报警闪光灯（能见度＜100米）。</li>
<li>减速至≤40km/h（高速），保持车距＞50米。</li>
</ul>
</li>
<li><strong>高速公路驾驶</strong>**</li>
</ul>
<ol>
<li><strong>车速与车距</strong>
<ul>
<li><strong>最低时速</strong>：60km/h。</li>
<li><strong>最高时速</strong>：小型客车120km/h，大型客车100km/h，摩托车80km/h。</li>
<li><strong>车道最低时速</strong>：左侧车道110km/h，中间车道90km/h，右侧车道60km/h。</li>
<li><strong>安全车距</strong>：时速100km/h时，车距100米以上。</li>
</ul>
</li>
<li><strong>特殊情况</strong>
<ul>
<li><strong>能见度小于200米</strong>：车速不超过60km/h，车距100米以上。</li>
<li><strong>能见度小于100米</strong>：车速不超过40km/h，车距50米以上。</li>
<li><strong>能见度小于50米</strong>：车速不超过20km/h，尽快驶离。</li>
</ul>
</li>
<li><strong>故障处理</strong>
<ul>
<li><strong>一般道路</strong>：车后50-100米放置警告标志。</li>
<li><strong>高速公路</strong>：车后150米外放置警告标志。</li>
</ul>
</li>
</ol>
<h4 id="_2-事故处理流程" tabindex="-1"><a class="header-anchor" href="#_2-事故处理流程" aria-hidden="true">#</a> <strong>2. 事故处理流程</strong></h4>
<ol>
<li><strong>立即停车</strong>：开启双闪，放置警示牌（普通道路50~100米，高速150米外）。</li>
<li><strong>保护现场</strong>：拍照取证（车辆位置、碰撞痕迹、环境标志）。</li>
<li><strong>救助伤员</strong>：拨打120，勿随意移动伤者（除非危及生命）。</li>
<li><strong>报警与保险</strong>：拨打122报警，48小时内通知保险公司。</li>
</ol>
<h4 id="_3-紧急情况应对" tabindex="-1"><a class="header-anchor" href="#_3-紧急情况应对" aria-hidden="true">#</a> <strong>3. 紧急情况应对</strong></h4>
<ul>
<li><strong>爆胎</strong>：
<ul>
<li>紧握方向盘，松油门，点刹减速，切勿急刹。</li>
<li>后轮爆胎可轻微修正方向，前轮爆胎需全力控制方向。</li>
</ul>
</li>
<li><strong>制动失灵</strong>：
<ul>
<li>抢挂低挡（逐级降档），拉手刹（缓慢拉起），摩擦护栏/山体减速。</li>
</ul>
</li>
<li><strong>车辆落水</strong>：
<ul>
<li>解锁车门/车窗，破窗逃生（头枕金属杆可破窗）。</li>
</ul>
</li>
</ul>
